"""8.Usando la condicional if imprimir por pantalla si una lista está vacía o no, probar con
una lista vacía y otra con una lista vacía."""

lista1 = []
lista2 = [1, 2, 3, 4, 5]

if not lista1:
    print("La lista vacía está vacía.")
else:
    print("La lista vacía no está vacía.")

if not lista2:
    print("La lista con elementos está vacía.")
else:
    print("La lista con elementos no está vacía.")
